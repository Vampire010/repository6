package Condition_Statements;

public class Right_TRi_ex
{

	public static void main(String[] args) 
	{
		// i is row , j is col
		
		int i, j;
		
		for(i = 0 ; i< 5;i++)
		{
			for(j=0;j<=i ;j++)
			{
				System.out.print("* ");
				
			}
			System.out.println();
	
		}				
	}

}



