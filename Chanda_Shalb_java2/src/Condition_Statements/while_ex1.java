package Condition_Statements;

public class while_ex1 
{

	public static void main(String[] args) 
	{
		int i =0 ;
		while( i<=10)
		{
			
			System.out.print(i + " ,");
			i++;
		}
		System.out.println();
		
		System.out.println("********________************");

		
		int j =10 ;
		while( j>=0)
		{
			
			System.out.print(j + " ,");
			j--;
		}

		
	}
			
}
