package Condition_Statements;

public class For_ex1 {

	public static void main(String[] args)
	{
			for(int i=5;i<=10;i++)
			{
				System.out.print(i + " , ");
			}
			System.out.println();
			System.out.println("*************_______________***************");

			
			for(int i=10;i>=5;i--)
			{
				System.out.print(i + " , ");
			}
			
	}

}







