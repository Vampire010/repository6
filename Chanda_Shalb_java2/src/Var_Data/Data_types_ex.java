package Var_Data;

public class Data_types_ex {

	public static void main(String[] args)
	{
		
		// byte = -128 to 127
		byte b= -12;
		System.out.println(b);
		
		// short -32768 to 32767
		short c= -32768;
		System.out.println(c);
		
		int a = 1177877887;
		System.out.println(a);
		
		long d = -4233220000000000000L;
		
		System.out.println(d);


				
	}

}
